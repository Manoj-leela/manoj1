package com.syml.purchaseProposal.couchbase;

public class ApplicantSummary {
	
	private String applicantFirstName;
	private String applicantLastName;
	private String applicantBeacon;
	private String applicantTotalIncome;
	private String applicantTotalLiabilities;
	private String applicantTotalLiabilityPayments;
	private String applicantTotalAssets;
	public String getApplicantFirstName() {
		return applicantFirstName;
	}
	public void setApplicantFirstName(String applicantFirstName) {
		this.applicantFirstName = applicantFirstName;
	}
	public String getApplicantLastName() {
		return applicantLastName;
	}
	public void setApplicantLastName(String applicantLastName) {
		this.applicantLastName = applicantLastName;
	}
	public String getApplicantBeacon() {
		return applicantBeacon;
	}
	public void setApplicantBeacon(String applicantBeacon) {
		this.applicantBeacon = applicantBeacon;
	}
	public String getApplicantTotalIncome() {
		return applicantTotalIncome;
	}
	public void setApplicantTotalIncome(String applicantTotalIncome) {
		this.applicantTotalIncome = applicantTotalIncome;
	}
	public String getApplicantTotalLiabilities() {
		return applicantTotalLiabilities;
	}
	public void setApplicantTotalLiabilities(String applicantTotalLiabilities) {
		this.applicantTotalLiabilities = applicantTotalLiabilities;
	}
	public String getApplicantTotalLiabilityPayments() {
		return applicantTotalLiabilityPayments;
	}
	public void setApplicantTotalLiabilityPayments(String applicantTotalLiabilityPayments) {
		this.applicantTotalLiabilityPayments = applicantTotalLiabilityPayments;
	}
	public String getApplicantTotalAssets() {
		return applicantTotalAssets;
	}
	public void setApplicantTotalAssets(String applicantTotalAssets) {
		this.applicantTotalAssets = applicantTotalAssets;
	}
	
	
	

}

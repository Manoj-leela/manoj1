package com.syml.referral;

public class RefferalSurveyException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = 6011398114146739040L;

	public RefferalSurveyException() {
		super();
		
	}

	public RefferalSurveyException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public RefferalSurveyException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public RefferalSurveyException(String message) {
		super(message);
		
	}

	public RefferalSurveyException(Throwable cause) {
		super(cause);
		
	}
}
